package com.alex.mirash.graph.graph.tool;

/**
 * @author Mirash
 */

public class GraphPoint {
    private float x;
    private float y;

    public GraphPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
